let hari = "Senin";

switch (hari) {
  case "Senin":
    console.log("Hari ini adalah awal pekan.");
    break;
  case "Jumat":
    console.log("Hari ini adalah menjelang akhir pekan.");
    break;
  case "Minggu":
    console.log("Hari ini adalah hari libur.");
    break;
  default:
    console.log("Hari biasa.");
}
// Output: Hari ini adalah awal pekan.
